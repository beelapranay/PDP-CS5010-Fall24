  /**
   * Generic method to test histogram generation by comparing with an expected histogram.
   */
  private void testHistogramGeneration(String imageName, RGB[][] imageData,
                                       Map<String, int[]> expectedHistogram) {
    controller.getImages().put(imageName, imageData);

    view.generateAndStoreHistogram(imageName, imageName + "Histogram");

    RGB[][] histogramImageData = controller.getImages().get(imageName + "Histogram");
    Map<String, int[]> actualHistogram = calculateHistogramData(histogramImageData);

    assertArrayEquals(expectedHistogram.get("Red"), actualHistogram.get("Red"));
    assertArrayEquals(expectedHistogram.get("Green"), actualHistogram.get("Green"));
    assertArrayEquals( expectedHistogram.get("Blue"), actualHistogram.get("Blue"));
  }

  /**
   * Helper method to calculate histogram data for each color channel from an RGB array.
   *
   * @param pixelArray the image's pixel data as an RGB array
   * @return a map with histogram arrays for each color channel
   */
  private Map<String, int[]> calculateHistogramData(RGB[][] pixelArray) {
    int[] redHistogram = new int[256];
    int[] greenHistogram = new int[256];
    int[] blueHistogram = new int[256];

    for (RGB[] row : pixelArray) {
      for (RGB pixel : row) {
        redHistogram[pixel.red]++;
        greenHistogram[pixel.green]++;
        blueHistogram[pixel.blue]++;
      }
    }

    Map<String, int[]> histogramData = new HashMap<>();
    histogramData.put("Red", redHistogram);
    histogramData.put("Green", greenHistogram);
    histogramData.put("Blue", blueHistogram);

    return histogramData;
  }
